def get_all_nodes():
    """Return unified list of all nodes as dicts."""
    nodes = []
    seen = set()
    for entry in INDIA_DISTRICTS + WORLD_CITIES:
        name = entry[0]
        if name.lower() in seen:
            continue
        seen.add(name.lower())
        nodes.append({
            "id":         name.lower().replace(" ", "_").replace("-","_").replace("'",""),
            "name":       name,
            "lat":        entry[1],
            "lon":        entry[2],
            "region":     entry[3],      # state (India) or country (world)
            "is_port":    entry[4],
            "is_airport": entry[5],
            "country":    "India" if entry[3] in INDIA_STATES else entry[3],
            "capabilities": NODE_CAPABILITIES.get(name, ["GENERAL"])
        })
    return nodes


